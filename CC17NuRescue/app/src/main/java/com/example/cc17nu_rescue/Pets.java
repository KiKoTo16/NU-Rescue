package com.example.cc17nu_rescue;

public class Pets {

    String name, breed, age, color, gender, weight, social;
    int imageId;

    public Pets(String name, String breed, String age, String gender, String color, String weight, String social, int imageId) {
        this.name = name;
        this.breed = breed;
        this.age = age;
        this.color = color;
        this.weight = weight;
        this.social = social;
        this.imageId = imageId;
        this.gender = gender;
    }
}
